package com.kyung.dto;

public class Category {
	int id;
	String name;
	int boardId;

	public int getId() 
	{
		return id;
	}
	public void setId(int id) 
	{
		this.id = id;
	}
	public String getName() 
	{
		return name;
	}
	public void setName(String name) 
	{
		this.name = name;
	}
	public int getBoardId() 
	{
		return boardId;
	}
	public void setBoardId(int boardId) 
	{
		this.boardId = boardId;
	}
}
